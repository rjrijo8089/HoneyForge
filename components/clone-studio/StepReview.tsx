interface StepReviewProps {
  data: WizardData
  onDeploy: () => void
  deploying: boolean
}

/* ─────────────────────────── config generators ─────────────────────────── */

function generateSnareYaml(data: WizardData): string {
  const { step1, step2 } = data
  const decoySlug = (step2.name || 'unnamed-decoy').toLowerCase().replace(/\s+/g, '-')
  return `# SNARE Configuration — Generated by HoneyForge Clone Studio
# Decoy: ${step2.name || 'Unnamed Decoy'}
# Source: ${step1.url || 'N/A'}
# WARNING: Deploy only on assets you own or are authorized to test

port: 8080
dir: /var/snap/
tanner: http://tanner:8090
page-dir: /opt/honeyforge/decoys/${decoySlug}
clone-url: "${step1.url || ''}"
clone-strategy: "${step1.cloneType}"
debug: false
slurp-delay: 0.0
slurp-size-limit: 5

decoy:
  name: "${step2.name || ''}"
  category: "${step2.category || ''}"
  department: "${step2.department || ''}"
  business_owner: "${step2.businessOwner || ''}"
  tags: [${(step2.tags || '').split(',').map((t) => `"${t.trim()}"`).filter(Boolean).join(', ')}]

rate_limit:
  enabled: true
  max_requests: 200
  window_seconds: 60

auth:
  required: ${step1.authRequired}${step1.authRequired ? `\n  username: "${step1.username}"` : ''}

tls:
  enabled: false
`
}

function generateTannerJson(data: WizardData): string {
  const { step3, step4 } = data
  const obj = {
    server:  { host: '0.0.0.0', port: 8090, debug: false, timeout: 30 },
    plugins: {
      sql_injection:     { enabled: step3.sqlInjection },
      xss:               { enabled: step3.xss },
      lfi:               { enabled: step3.lfi },
      rfi:               { enabled: step3.rfi },
      cmd_injection:     { enabled: step3.commandInjection },
      login_attempt:     { enabled: step3.credentialLogging },
      path_traversal:    { enabled: step3.directoryTraversal },
      payload_capture:   { enabled: step3.payloadCapture },
      useragent_capture: { enabled: step3.userAgentCapture },
      cookie_capture:    { enabled: step3.cookieCapture },
      header_capture:    { enabled: step3.headerCapture },
    },
    output: {
      opensearch: { enabled: step4.opensearch.enabled, host: step4.opensearch.host, port: step4.opensearch.port },
      splunk:     { enabled: step4.splunk.enabled,     hec_url: step4.splunk.hecUrl, token: step4.splunk.token ? '***' : '' },
      sentinel:   { enabled: step4.sentinel.enabled,   workspace_id: step4.sentinel.workspaceId, shared_key: step4.sentinel.sharedKey ? '***' : '' },
      qradar:     { enabled: step4.qradar.enabled,     host: step4.qradar.host, port: step4.qradar.port },
      misp:       { enabled: step4.misp.enabled,       url: step4.misp.url, api_key: step4.misp.apiKey ? '***' : '' },
      slack:      { enabled: step4.slack.enabled,      webhook_url: step4.slack.webhookUrl ? '***' : '' },
      email:      { enabled: step4.email.enabled,      address: step4.email.address },
      webhook:    { enabled: step4.webhook.enabled,    url: step4.webhook.url },
    },
  }
  return JSON.stringify(obj, null, 2)
}

/* ──────────────────────────── CodeBlock ──────────────────────────── */

function CodeBlock({ code, lang }: { code: string; lang: string }) {
  const [copied, setCopied] = useState(false)
  const handleCopy = () => {
    navigator.clipboard.writeText(code).then(() => {
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    })
  }
  return (
    <div className="rounded-xl border border-hf-border/40 overflow-hidden">
      <div className="flex items-center justify-between px-4 py-2 bg-hf-surface-3 border-b border-hf-border/40">
        <span className="text-[10px] font-mono font-semibold text-hf-dim">{lang}</span>
        <button onClick={handleCopy} className="flex items-center gap-1.5 text-[10px] text-hf-muted hover:text-hf-text transition-colors">
          {copied ? <CheckCheck className="w-3 h-3 text-hf-success" /> : <Copy className="w-3 h-3" />}
          {copied ? 'Copied!' : 'Copy'}
        </button>
      </div>
      <pre className="p-4 overflow-x-auto text-[11px] font-mono text-hf-muted leading-relaxed bg-hf-bg-2 max-h-72">
        <code>{code}</code>
      </pre>
    </div>
  )
}

