/* ─────────────────────────── Summary Row ────────────────────────────── */

function SummaryRow({ label, value, accent = false }: { label: string; value: string; accent?: boolean }) {
  return (
    <div className="flex items-center gap-3 py-2 border-b border-hf-border/20 last:border-0">
      <span className="text-[11px] text-hf-dim w-36 shrink-0">{label}</span>
      <span className={cn('text-xs font-medium truncate', accent ? 'text-hf-primary' : 'text-hf-text')}>
        {value || <span className="text-hf-dim italic">Not set</span>}
      </span>
    </div>
  )
}

/* ─────────────────────────── Collapsible Section ─────────────────────── */

function Section({ title, badge, children }: { title: string; badge?: string; children: React.ReactNode }) {
  const [open, setOpen] = useState(true)
  return (
    <div className="glass-card rounded-2xl border border-hf-border/40 overflow-hidden">
      <button
        className="w-full flex items-center justify-between px-4 py-3 hover:bg-hf-surface-2/30 transition-colors"
        onClick={() => setOpen(!open)}
      >
        <div className="flex items-center gap-2">
          <h4 className="text-xs font-bold text-hf-text">{title}</h4>
          {badge && (
            <span className="text-[9px] font-semibold text-hf-muted bg-hf-surface-3 border border-hf-border/40 px-1.5 py-0.5 rounded">
              {badge}
            </span>
          )}
        </div>
        {open ? <ChevronUp className="w-4 h-4 text-hf-dim" /> : <ChevronDown className="w-4 h-4 text-hf-dim" />}
      </button>
      {open && <div className="px-4 pb-4">{children}</div>}
    </div>
  )
}

/* ─────────────────────────── Main Component ─────────────────────────── */

export function StepReview({ data, onDeploy, deploying }: StepReviewProps) {
  const enabledDetections = Object.values(data.step3).filter(Boolean).length
  const enabledIntegrations = (Object.values(data.step4) as { enabled: boolean }[]).filter((v) => v.enabled).length

  const snareYaml  = generateSnareYaml(data)
  const tannerJson = generateTannerJson(data)

  return (
    <div className="space-y-5">

      {/* Config summary */}
      <Section title="Clone Source" badge={data.step1.cloneType}>
        <SummaryRow label="Source URL"     value={data.step1.url}      accent />
        <SummaryRow label="Clone Strategy" value={data.step1.cloneType} />
        <SummaryRow label="Auth Required"  value={data.step1.authRequired ? 'Yes' : 'No'} />
        {data.step1.analyzed && (
          <SummaryRow
            label="Detected"
            value={`${data.step1.detectedPages} pages, ${data.step1.detectedForms} forms, ${data.step1.detectedLinks} links`}
          />
        )}
      </Section>

      <Section title="Decoy Configuration">
        <SummaryRow label="Name"            value={data.step2.name}         accent />
        <SummaryRow label="Category"        value={data.step2.category} />
        <SummaryRow label="Department"      value={data.step2.department} />
        <SummaryRow label="Business Owner"  value={data.step2.businessOwner} />
        {data.step2.tags && <SummaryRow label="Tags" value={data.step2.tags} />}
      </Section>

      <Section title="Attack Monitoring" badge={`${enabledDetections} rules`}>
        <div className="flex flex-wrap gap-1.5 pt-1">
          {(Object.entries(data.step3) as [string, boolean][]).map(([key, enabled]) => {
            const label: Record<string, string> = {
              sqlInjection: 'SQLi', xss: 'XSS', lfi: 'LFI', rfi: 'RFI',
              commandInjection: 'CMDi', credentialLogging: 'Credentials',
              directoryTraversal: 'Dir Traversal', payloadCapture: 'Payloads',
              userAgentCapture: 'User-Agent', cookieCapture: 'Cookies', headerCapture: 'Headers',
            }
            return (
              <span
                key={key}
                className={cn(
                  'text-[10px] font-medium px-2 py-0.5 rounded border',
                  enabled
                    ? 'text-hf-success bg-hf-success/10 border-hf-success/30'
                    : 'text-hf-dim bg-hf-surface-3 border-hf-border/30 line-through'
                )}
              >
                {label[key] ?? key}
              </span>
            )
          })}
        </div>
      </Section>

      <Section title="Integrations" badge={`${enabledIntegrations} enabled`}>
        <div className="flex flex-wrap gap-1.5 pt-1">
          {(Object.entries(data.step4) as [string, { enabled: boolean }][]).map(([key, val]) => {
            const label: Record<string, string> = {
              opensearch: 'OpenSearch', splunk: 'Splunk', sentinel: 'Sentinel', qradar: 'QRadar',
              misp: 'MISP', slack: 'Slack', email: 'Email', webhook: 'Webhook',
            }
            return (
              <span
                key={key}
                className={cn(
                  'text-[10px] font-medium px-2 py-0.5 rounded border',
                  val.enabled
                    ? 'text-hf-primary bg-hf-primary/10 border-hf-primary/30'
                    : 'text-hf-dim bg-hf-surface-3 border-hf-border/30'
                )}
              >
                {label[key] ?? key}
              </span>
            )
          })}
        </div>
      </Section>

      {/* Architecture */}
      <div>
        <p className="text-[10px] font-bold text-hf-dim uppercase tracking-widest mb-2 px-1">Deployment Architecture</p>
        <ArchDiagram data={data} />
      </div>

      {/* SNARE config */}
      <div>
        <p className="text-[10px] font-bold text-hf-dim uppercase tracking-widest mb-2 px-1">Generated SNARE Config (YAML)</p>
        <CodeBlock code={snareYaml} lang="snare.yaml" />
      </div>

      {/* TANNER config */}
      <div>
        <p className="text-[10px] font-bold text-hf-dim uppercase tracking-widest mb-2 px-1">Generated TANNER Config (JSON)</p>
        <CodeBlock code={tannerJson} lang="tanner.json" />
      </div>

      {/* Deploy */}
      <div className="flex justify-end pt-2">
        <Button
          variant="primary"
          size="lg"
          isLoading={deploying}
          disabled={!data.step2.name || !data.step1.url}
          onClick={onDeploy}
          leftIcon={<Rocket className="w-4 h-4" />}
        >
          {deploying ? 'Deploying…' : 'Deploy Decoy'}
        </Button>
      </div>

      {(!data.step2.name || !data.step1.url) && (
        <p className="text-xs text-hf-warning text-right -mt-2">
          Complete Step 1 (URL) and Step 2 (Name) before deploying.
        </p>
      )}
    </div>
  )
}
